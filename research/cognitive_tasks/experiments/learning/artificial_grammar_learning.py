"""
Artificial Grammar Learning implementation.

Artificial Grammar Learning (AGL) measures implicit learning of complex rules.
Participants study strings generated by an artificial grammar and are then tested
on their ability to distinguish grammatical from ungrammatical strings.
Learning is typically implicit and participants cannot verbalize the rules.

APGI Integration:
- θₜ (threshold): Grammar classification threshold
- π (precision): Rule learning precision
- ε (prediction error): Grammar violation prediction error
- β (inverse temperature): Classification consistency
"""

import random
import numpy as np
import pandas as pd
from typing import Dict, List, Any, Optional, Tuple
from dataclasses import dataclass
import time

from .. import TrialBasedTask, TrialBasedTaskConfig


@dataclass
class ArtificialGrammarLearningConfig(TrialBasedTaskConfig):
    """Configuration for Artificial Grammar Learning experiment."""

    n_study_trials: int = 120  # Number of study strings
    n_test_trials: int = 60  # Number of test strings
    study_duration_ms: int = 5000  # Time to study each string
    test_duration_ms: int = 3000  # Time for test strings
    response_window_ms: int = 5000

    # Grammar parameters
    grammar_type: str = "finite_state"  # Type of artificial grammar
    alphabet: List[str] = None  # Symbols in the grammar
    min_string_length: int = 8  # Minimum string length
    max_string_length: int = 12  # Maximum string length

    # Test parameters
    grammatical_ratio: float = 0.5  # Proportion of grammatical test items
    feedback_type: str = "none"  # none, accuracy, confidence

    def __post_init__(self):
        if self.alphabet is None:
            self.alphabet = ["X", "Y", "Z", "W", "V", "T"]


class ArtificialGrammar:
    """Generate artificial grammar and strings."""

    def __init__(self, config: ArtificialGrammarLearningConfig):
        self.config = config
        self.grammar_rules = self._generate_grammar_rules()

    def _generate_grammar_rules(self) -> Dict[str, Any]:
        """Generate artificial grammar rules."""
        if self.config.grammar_type == "finite_state":
            # Reber grammar (1976) - classic artificial grammar
            rules = {
                "start": "S",
                "states": ["A", "B"],
                "terminals": self.config.alphabet,
                "rules": [
                    ("S", "A"),
                    ("A", "T"),
                    ("A", "V"),
                    ("B", "P"),
                    ("B", "X"),
                    ("B", "V"),
                    ("B", "T"),
                    ("A", "B"),
                    ("B", "S"),
                    ("T", "T"),  # Terminal
                    ("V", "V"),  # Terminal
                    ("P", "P"),  # Terminal
                    ("X", "X"),  # Terminal
                    ("W", "W"),  # Terminal
                ],
            }

            # Define state transitions
            state_transitions = {
                "A": {"T": "B", "V": "B"},
                "B": {"P": "B", "X": "B", "V": "B", "T": "S"},
            }

            rules["state_transitions"] = state_transitions
            rules["valid_end_states"] = ["S"]

            return rules
        else:
            # Default to simple grammar
            return {
                "start": "S",
                "states": ["A"],
                "terminals": self.config.alphabet,
                "rules": [("S", "A"), ("A", "X"), ("A", "Y"), ("X", "X"), ("Y", "Y")],
            }

    def generate_string(self, max_length: int = None) -> str:
        """Generate a grammatical string using the grammar rules."""
        if max_length is None:
            max_length = random.randint(
                self.config.min_string_length, self.config.max_string_length
            )
        else:
            max_length = min(max_length, self.config.max_string_length)

        string = []
        current_state = self.grammar_rules["start"]

        for _ in range(max_length):
            # Get possible transitions from current state
            if current_state in self.grammar_rules.get("state_transitions", {}):
                possible_transitions = self.grammar_rules["state_transitions"][
                    current_state
                ]

                if possible_transitions:
                    # Choose a transition
                    next_symbol = random.choice(list(possible_transitions.keys()))
                    string.append(next_symbol)

                    # Update state
                    current_state = possible_transitions[next_symbol]
                else:
                    break  # No more transitions
            else:
                # If no state transitions, add a terminal
                if current_state in self.grammar_rules["states"]:
                    string.append(random.choice(self.config.alphabet))
                break

        return "".join(string) if string else random.choice(self.config.alphabet)

    def is_grammatical(self, string: str) -> bool:
        """Check if a string follows the grammar rules."""
        current_state = self.grammar_rules["start"]

        for symbol in string:
            if current_state in self.grammar_rules.get("state_transitions", {}):
                possible_transitions = self.grammar_rules["state_transitions"][
                    current_state
                ]

                if symbol in possible_transitions:
                    current_state = possible_transitions[symbol]
                else:
                    # Symbol not allowed in this state
                    return False
            else:
                # Terminal symbol
                if symbol in self.config.alphabet:
                    continue
                else:
                    return False

        # Check if we ended in a valid state
        return current_state in self.grammar_rules.get(
            "valid_end_states", [self.grammar_rules["start"]]
        )

    def generate_test_strings(
        self, n_grammatical: int, n_ungrammatical: int
    ) -> List[Dict[str, Any]]:
        """Generate grammatical and ungrammatical test strings."""
        test_strings = []

        # Generate grammatical strings
        for _ in range(n_grammatical):
            string = self.generate_string()
            test_strings.append(
                {"string": string, "is_grammatical": True, "length": len(string)}
            )

        # Generate ungrammatical strings
        for _ in range(n_ungrammatical):
            # Create ungrammatical by violating grammar rules
            string = self.generate_string()

            # Introduce a grammar violation
            if len(string) > 2:
                # Swap two adjacent symbols to break sequence
                string_list = list(string)
                swap_pos = random.randint(1, len(string_list) - 2)
                string_list[swap_pos], string_list[swap_pos + 1] = (
                    string_list[swap_pos + 1],
                    string_list[swap_pos],
                )
                string = "".join(string_list)

            # Ensure it's actually ungrammatical
            if self.is_grammatical(string) and len(string) > 0:
                # Add a completely random symbol
                string_list = list(string)
                if len(string_list) > 0:
                    string_list[random.randint(0, len(string_list) - 1)] = (
                        random.choice(self.config.alphabet)
                    )
                    string = "".join(string_list)

            test_strings.append(
                {"string": string, "is_grammatical": False, "length": len(string)}
            )

        return test_strings


class ArtificialGrammarLearningTask(TrialBasedTask):
    """Artificial Grammar Learning task implementation."""

    def __init__(self, config: Optional[ArtificialGrammarLearningConfig] = None):
        super().__init__(config)
        self.config = config or ArtificialGrammarLearningConfig()
        self.grammar = ArtificialGrammar(self.config)
        self.learning_data = {}
        self.phase = "study"  # Start with study phase
        self.current_trial_data = {}  # Add missing attribute
        self.current_response_data = {}  # Add missing attribute

    def setup(self, **kwargs):
        """Set up the Artificial Grammar Learning task."""
        super().setup(**kwargs)
        self.grammar = ArtificialGrammar(self.config)
        self.learning_data = {}
        self.phase = "study"

    def run_trial(self, participant_id: int, trial_params: Dict) -> Dict[str, Any]:
        """Override to update current_trial_data."""
        # Update current_trial_data and current_response_data before processing
        self.current_trial_data = trial_params
        self.current_response_data = trial_params
        # Call parent run_trial
        return super().run_trial(participant_id, trial_params)

    def generate_trial_sequence(self) -> List[Dict[str, Any]]:
        """Generate trial sequence with study and test phases."""
        trials = []

        # Study phase
        study_strings = []
        for i in range(self.config.n_study_trials):
            string = self.grammar.generate_string()
            study_strings.append(string)

        for i, string in enumerate(study_strings):
            trials.append(
                {
                    "trial_number": i,
                    "phase": "study",
                    "string": string,
                    "is_grammatical": True,  # All study strings are grammatical
                    "condition": "study",
                    "length": len(string),  # Add length key
                }
            )

        # Test phase
        n_grammatical = int(self.config.n_test_trials * self.config.grammatical_ratio)
        n_ungrammatical = self.config.n_test_trials - n_grammatical

        test_strings = self.grammar.generate_test_strings(
            n_grammatical, n_ungrammatical
        )
        random.shuffle(test_strings)

        for i, test_string in enumerate(test_strings):
            trials.append(
                {
                    "trial_number": len(study_strings) + i,
                    "phase": "test",
                    "string": test_string["string"],
                    "is_grammatical": test_string["is_grammatical"],
                    "length": test_string["length"],
                    "condition": (
                        "test_grammatical"
                        if test_string["is_grammatical"]
                        else "test_ungrammatical"
                    ),
                }
            )

        return trials

    def generate_trial_parameters(
        self, participant_id: int, trial_number: int
    ) -> Dict[str, Any]:
        """Generate parameters for a specific trial."""
        base_params = super().generate_trial_parameters(participant_id, trial_number)

        # Get trial from sequence
        if trial_number < len(self.trial_sequence):
            trial_data = self.trial_sequence[trial_number]
        else:
            # Default trial
            trial_data = {
                "phase": "test",
                "string": "XTV",
                "is_grammatical": True,
                "condition": "test_grammatical",
                "length": len("XTV"),  # Add missing length key
            }

        base_params.update(
            {
                "phase": trial_data["phase"],
                "string": trial_data["string"],
                "is_grammatical": trial_data["is_grammatical"],
                "length": trial_data["length"],
                "condition": trial_data["condition"],
                "study_duration_ms": (
                    self.config.study_duration_ms
                    if trial_data["phase"] == "study"
                    else self.config.test_duration_ms
                ),
                "response_window_ms": self.config.response_window_ms,
            }
        )

        return base_params

    def process_response(self, response: Any, correct_response: Any) -> Dict[str, Any]:
        """Process Artificial Grammar Learning response."""
        # Handle different response formats
        if isinstance(response, str):
            response_str = response.lower()
        elif isinstance(response, bool):
            response_str = "yes" if response else "no"
        else:
            response_str = str(response).lower()

        # For test phase, correct response is based on grammaticality
        if self.current_trial_data.get("phase") == "test":
            is_grammatical = self.current_trial_data.get("is_grammatical", True)
            correct_str = "yes" if is_grammatical else "no"
        else:
            # Study phase: always correct (just exposure)
            correct_str = "yes"
            is_grammatical = True

        is_correct = response_str == correct_str

        # Store learning data
        phase = self.current_trial_data.get("phase", "study")
        condition = self.current_trial_data.get("condition", "study")

        key = f"{phase}_{condition}"
        if key not in self.learning_data:
            self.learning_data[key] = {
                "accuracy": [],
                "confidence": [],
                "response_times": [],
            }

        self.learning_data[key]["accuracy"].append(1 if is_correct else 0)
        self.learning_data[key]["confidence"].append(
            self.current_response_data.get("confidence", 0.5)
        )

        if self.current_response_data.get("reaction_time_ms", 0) > 0:
            self.learning_data[key]["response_times"].append(
                self.current_response_data.get("reaction_time_ms", 0)
            )

        # Calculate confidence
        base_confidence = 0.6  # Low confidence in implicit learning

        if phase == "test":
            if is_grammatical:
                base_confidence += 0.1  # Slightly higher for grammatical
            else:
                base_confidence -= 0.1  # Lower for ungrammatical

        return {
            "response": response_str,
            "accuracy": 1 if is_correct else 0,
            "reaction_time_ms": self.current_response_data.get("reaction_time_ms", 0),
            "confidence": base_confidence,
            "string": self.current_trial_data.get("string"),
            "is_grammatical": is_grammatical,
            "length": self.current_trial_data.get("length"),
            "phase": phase,
            "condition": condition,
            "correct_response": correct_str,
        }

    def simulate_response(self, trial_data: Dict[str, Any]) -> Dict[str, Any]:
        """Simulate participant Artificial Grammar Learning performance."""
        phase = trial_data["phase"]
        is_grammatical = trial_data.get("is_grammatical", True)
        string = trial_data["string"]

        if phase == "study":
            # Study phase: always "yes" (just exposure)
            return {
                "response": "yes",
                "reaction_time_ms": 100,  # Minimal RT for study
                "confidence": 0.5,
                "timestamp": time.time(),
            }

        # Test phase: classification task
        # Base classification accuracy depends on string length and grammaticality
        base_accuracy = 0.7

        # Shorter strings are easier to classify
        length_factor = (
            1.0
            - (
                (len(string) - self.config.min_string_length)
                / (self.config.max_string_length - self.config.min_string_length + 1)
            )
            * 0.2
        )
        base_accuracy += length_factor

        # Grammatical strings are classified more accurately (implicit learning)
        if is_grammatical:
            base_accuracy += 0.15  # Learning effect
        else:
            base_accuracy -= 0.05  # Ungrammatical are harder

        base_accuracy = np.clip(base_accuracy, 0.3, 0.95)

        # Generate response
        if random.random() < base_accuracy:
            response = "yes" if is_grammatical else "no"
        else:
            response = "no" if is_grammatical else "yes"

        # Calculate reaction time
        base_rt = 1200

        # Faster for grammatical strings (familiarity)
        if is_grammatical:
            base_rt -= 200

        # Slower for longer strings
        base_rt += (len(string) - 8) * 50

        rt = max(400, np.random.normal(base_rt, 250))

        # Calculate confidence
        actual_correct = response == ("yes" if is_grammatical else "no")
        confidence = 0.6 if actual_correct else 0.3

        if is_grammatical and actual_correct:
            confidence += 0.2  # Higher confidence for correctly identified grammatical
        elif not is_grammatical and actual_correct:
            confidence += (
                0.1  # Moderate confidence for correctly identified ungrammatical
            )

        return {
            "response": response,
            "reaction_time_ms": int(rt),
            "confidence": confidence,
            "timestamp": time.time(),
        }

    def compute_participant_summary(
        self, trials: List[Dict[str, Any]]
    ) -> Dict[str, Any]:
        """Compute comprehensive summary for Artificial Grammar Learning task."""
        base_summary = super().compute_participant_summary(trials)

        if not trials:
            return base_summary

        df = pd.DataFrame(trials)

        # Artificial Grammar Learning specific metrics
        summary = {
            **base_summary,
            "overall_accuracy": 0,
            "mean_reaction_time": 0,
            "grammar_learning": {},
            "phase_performance": {},
            "implicit_learning": {},
        }

        # Filter test phase for main analysis
        test_trials = df[df["phase"] == "test"] if "phase" in df.columns else df

        # Overall metrics
        if "accuracy" in test_trials.columns:
            summary["overall_accuracy"] = test_trials["accuracy"].mean()

        if "reaction_time_ms" in test_trials.columns:
            summary["mean_reaction_time"] = test_trials["reaction_time_ms"].mean()

        # Performance by grammaticality
        grammatical_trials = test_trials[test_trials["is_grammatical"] == True]
        ungrammatical_trials = test_trials[test_trials["is_grammatical"] == False]

        if len(grammatical_trials) > 0 and len(ungrammatical_trials) > 0:
            grammatical_acc = (
                grammatical_trials["accuracy"].mean()
                if "accuracy" in grammatical_trials.columns
                else 0
            )
            ungrammatical_acc = (
                ungrammatical_trials["accuracy"].mean()
                if "accuracy" in ungrammatical_trials.columns
                else 0
            )
            grammatical_rt = (
                grammatical_trials["reaction_time_ms"].mean()
                if "reaction_time_ms" in grammatical_trials.columns
                else 0
            )
            ungrammatical_rt = (
                ungrammatical_trials["reaction_time_ms"].mean()
                if "reaction_time_ms" in ungrammatical_trials.columns
                else 0
            )

            summary["grammar_learning"] = {
                "grammatical_accuracy": grammatical_acc,
                "ungrammatical_accuracy": ungrammatical_acc,
                "grammatical_rt": grammatical_rt,
                "ungrammatical_rt": ungrammatical_rt,
                "discrimination_index": grammatical_acc - ungrammatical_acc,
                "rt_difference": ungrammatical_rt - grammatical_rt,
            }

        # Performance by condition
        for condition in test_trials["condition"].unique():
            condition_data = test_trials[test_trials["condition"] == condition]
            if len(condition_data) > 0:
                accuracy = (
                    condition_data["accuracy"].mean()
                    if "accuracy" in condition_data.columns
                    else 0
                )
                mean_rt = (
                    condition_data["reaction_time_ms"].mean()
                    if "reaction_time_ms" in condition_data.columns
                    else 0
                )
                mean_confidence = (
                    condition_data["confidence"].mean()
                    if "confidence" in condition_data.columns
                    else 0
                )

                summary["phase_performance"][condition] = {
                    "accuracy": accuracy,
                    "mean_rt": mean_rt,
                    "mean_confidence": mean_confidence,
                    "n_trials": len(condition_data),
                }

        # Implicit learning measures
        if len(grammatical_trials) > 0:
            # Length effects on classification
            length_effects = {}
            for length in range(
                self.config.min_string_length, self.config.max_string_length + 1
            ):
                if "length" in grammatical_trials.columns:
                    length_data = grammatical_trials[
                        grammatical_trials["length"] == length
                    ]
                    if len(length_data) > 0:
                        length_effects[f"length_{length}"] = {
                            "accuracy": (
                                length_data["accuracy"].mean()
                                if "accuracy" in length_data.columns
                                else 0
                            ),
                            "n_trials": len(length_data),
                        }

            summary["implicit_learning"] = {
                "length_effects": length_effects,
                "learning_slope": self._calculate_learning_slope_by_length(
                    length_effects
                ),
            }

        return summary

    def _calculate_learning_slope_by_length(
        self, length_effects: Dict[str, Dict]
    ) -> float:
        """Calculate learning slope as function of string length."""
        if len(length_effects) < 3:
            return 0.0

        lengths = []
        accuracies = []

        for key, data in length_effects.items():
            if key.startswith("length_"):
                length = int(key.split("_")[1])
                lengths.append(length)
                accuracies.append(data["accuracy"])

        if len(lengths) > 1:
            slope, _ = np.polyfit(lengths, accuracies, 1)
            return slope

        return 0.0


def run_artificial_grammar_learning_experiment(**kwargs):
    """Run the Artificial Grammar Learning experiment."""
    # Create config with provided parameters
    config_params = {}

    # Map common parameters
    param_mapping = {
        "n_participants": "n_participants",
        "n_trials": "n_study_trials",  # Use study trials as primary metric
        "n_trials_per_condition": "n_study_trials",
    }

    for key, value in kwargs.items():
        config_key = param_mapping.get(key, key)
        if hasattr(ArtificialGrammarLearningConfig, config_key):
            config_params[config_key] = value

    # Create config and experiment
    config = ArtificialGrammarLearningConfig(**config_params)
    experiment = ArtificialGrammarLearningTask(config)

    # Set up and run experiment
    experiment.setup()
    results = experiment.run_experiment()

    # Save results if output file specified
    if kwargs.get("output_file"):
        experiment.save_data(kwargs["output_file"])

    return experiment


